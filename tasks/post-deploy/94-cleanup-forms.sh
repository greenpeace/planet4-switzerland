if [ -z "$FORM_IDS" ]; then
	echo "No form IDs provided. Stopping execution."
	exit 0
fi

echo "Clean up content by trashing forms with IDs: ${FORM_IDS}"

for form_id in $FORM_IDS; do
	# Gravity Forms stores trash state as an is_trash flag.
	form_json="$(wp gf form get "$form_id" 2>/dev/null)"

	if printf '%s' "$form_json" | grep -Eq '"is_trash"[[:space:]]*:[[:space:]]*"?1"?'; then
		echo "Form ID $form_id is already in trash. Skipping."
		continue
	fi

	echo "Trashing form ID $form_id"
	wp gf form delete "$form_id"
done



